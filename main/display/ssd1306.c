/* ── I2C helpers ───────────────────────────────────────────────── */

static esp_err_t cmd_byte(uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };   /* Co=0, D/C#=0 (command) */
    return i2c_master_transmit(s_dev, buf, 2, 100);
}

static esp_err_t cmd_bytes(const uint8_t *cmds, size_t len)
{
    for (size_t i = 0; i < len; i++) {
        esp_err_t ret = cmd_byte(cmds[i]);
        if (ret != ESP_OK) return ret;
    }
    return ESP_OK;
}

/* ── Public API ────────────────────────────────────────────────── */

esp_err_t ssd1306_init(i2c_master_bus_handle_t bus, uint8_t addr)
{
    /* Allocate framebuffer from PSRAM */
    s_fb = ps_calloc(1, FB_SIZE);
    if (!s_fb) {
        ESP_LOGE(TAG, "Framebuffer alloc failed (%d bytes)", FB_SIZE);
        return ESP_ERR_NO_MEM;
    }

    /* Add device to I2C bus */
    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address  = addr,
        .scl_speed_hz    = LANG_I2C_FREQ_HZ,
    };
    esp_err_t ret = i2c_master_bus_add_device(bus, &dev_cfg, &s_dev);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "I2C add device 0x%02X failed: %s", addr, esp_err_to_name(ret));
        return ret;
    }

    /* Init sequence for 128x64 SSD1306/SSD1315.
     * SSD1315 is register-compatible but needs different contrast/precharge/VCOMH.
     * Send all commands in one I2C transaction for reliability. */

    /* Phase 1: Display OFF + basic config + charge pump */
    static const uint8_t init1[] = {
        0xAE,       /* Display OFF */
        0xD5, 0x80, /* Clock divide ratio / oscillator frequency */
        0xA8, 0x3F, /* Multiplex ratio: 64 */
        0xD3, 0x00, /* Display offset: 0 */
        0x40,       /* Start line: 0 */
        0x8D, 0x14, /* Charge pump: enable (internal VCC) */
    };
    ret = cmd_bytes(init1, sizeof(init1));
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "SSD1306 init phase 1 failed: %s", esp_err_to_name(ret));
        return ret;
    }
    ESP_LOGI(TAG, "SSD1306 init phase 1 OK (display off, charge pump)");

    /* Let charge pump stabilise — SSD1315 datasheet recommends 100ms */
    vTaskDelay(pdMS_TO_TICKS(100));

    /* Phase 2: Memory mode, scan direction, contrast, timings */
    static const uint8_t init2[] = {
        0x20, 0x00, /* Memory addressing: horizontal */
        0xA1,       /* Segment re-map: column 127 = SEG0 */
        0xC8,       /* COM scan direction: remapped */
        0xDA, 0x12, /* COM pins config: alternative, no L/R remap */
        0x81, 0x7F, /* Contrast: 127 (works for both SSD1306 and SSD1315) */
        0xD9, 0x22, /* Pre-charge period: phase1=2, phase2=2 (SSD1315 compatible) */
        0xDB, 0x20, /* VCOMH deselect level: 0.77×Vcc (SSD1315 compatible) */
        0xA4,       /* Resume from GDDRAM */
        0xA6,       /* Normal display (not inverted) */
    };
    ret = cmd_bytes(init2, sizeof(init2));
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "SSD1306 init phase 2 failed: %s", esp_err_to_name(ret));
        return ret;
    }
    ESP_LOGI(TAG, "SSD1306 init phase 2 OK (config + contrast)");

    /* Clear GDDRAM before turning display on */
    ssd1306_clear();
    ret = ssd1306_refresh();
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "SSD1306 initial refresh failed: %s", esp_err_to_name(ret));
        return ret;
    }

    /* Phase 3: Display ON */
    ret = cmd_byte(0xAF);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "SSD1306 display ON failed: %s", esp_err_to_name(ret));
        return ret;
    }

    ESP_LOGI(TAG, "SSD1306 %dx%d OLED ready (I2C addr 0x%02X)", W, H, addr);
    return ESP_OK;
}

void ssd1306_clear(void)
{
    if (s_fb) memset(s_fb, 0, FB_SIZE);
}

esp_err_t ssd1306_refresh(void)
{
    if (!s_dev || !s_fb) return ESP_ERR_INVALID_STATE;

    /* Set column and page address to cover entire display */
    static const uint8_t addr_cmds[] = {
        0x21, 0x00, 0x7F,  /* Column start=0, end=127 */
        0x22, 0x00, 0x07,  /* Page start=0, end=7 */
    };
    esp_err_t ret = cmd_bytes(addr_cmds, sizeof(addr_cmds));
    if (ret != ESP_OK) return ret;

    /* Send framebuffer as data (Co=0, D/C#=1 = 0x40 prefix).
     * Buffer MUST be in SRAM — I2C DMA cannot source from PSRAM. */
    uint8_t *tx = malloc(FB_SIZE + 1);
    if (!tx) return ESP_ERR_NO_MEM;
    tx[0] = 0x40;  /* data mode */
    memcpy(tx + 1, s_fb, FB_SIZE);
    ret = i2c_master_transmit(s_dev, tx, FB_SIZE + 1, 200);
    free(tx);
    return ret;
}

void ssd1306_pixel(int x, int y, bool on)
{
    if (!s_fb || x < 0 || x >= W || y < 0 || y >= H) return;
    int page = y / 8;
    int bit  = y % 8;
    if (on)
        s_fb[page * W + x] |=  (1 << bit);
    else
        s_fb[page * W + x] &= ~(1 << bit);
}

int ssd1306_char(int x, int y, char c)
{
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = font6x8[c - 32];
    for (int col = 0; col < 6; col++) {
        uint8_t bits = glyph[col];
        for (int row = 0; row < 8; row++) {
            ssd1306_pixel(x + col, y + row, bits & (1 << row));
        }
    }
    return x + 6;
}

int ssd1306_str(int x, int y, const char *s)
{
    while (*s && x < W) {
        x = ssd1306_char(x, y, *s++);
    }
    return x;
}

int ssd1306_str_2x(int x, int y, const char *s)
{
    /* Draw each character at 2x vertical scale (6 wide x 16 tall) */
    while (*s && x < W) {
        char c = *s++;
        if (c < 32 || c > 126) c = '?';
        const uint8_t *glyph = font6x8[c - 32];
        for (int col = 0; col < 6; col++) {
            uint8_t bits = glyph[col];
            for (int row = 0; row < 8; row++) {
                bool on = bits & (1 << row);
                ssd1306_pixel(x + col, y + row * 2,     on);
                ssd1306_pixel(x + col, y + row * 2 + 1, on);
            }
        }
        x += 6;
    }
    return x;
}

void ssd1306_fill_rect(int x, int y, int w, int h, bool on)
{
    for (int dy = 0; dy < h; dy++) {
        for (int dx = 0; dx < w; dx++) {
            ssd1306_pixel(x + dx, y + dy, on);
        }
    }
}

void ssd1306_hline(int x, int y, int w, bool on)
{
    for (int dx = 0; dx < w; dx++) {
        ssd1306_pixel(x + dx, y, on);
    }
}

esp_err_t ssd1306_set_contrast(uint8_t val)
{
    uint8_t cmds[] = { 0x81, val };
    return cmd_bytes(cmds, 2);
}

esp_err_t ssd1306_test_pattern(void)
{
    if (!s_fb) return ESP_ERR_INVALID_STATE;
    /* Fill entire framebuffer with 0xFF (all pixels on) */
    memset(s_fb, 0xFF, FB_SIZE);
    esp_err_t ret = ssd1306_refresh();
    ESP_LOGI(TAG, "Test pattern (all white): %s", esp_err_to_name(ret));
    return ret;
}
